#include <iostream>

short number_of_exits = 0;

std::string maze[25] =              // [27]
{
    "####B######################",
    "# #       #   #      #    #", //1 . 25 = C
    "# # # ###### #### ####### #",
    "# # # #       #   #       #",
    "#   # # ######### # ##### #",
    "# # # #   #       #     # #",
    "### ### ### ############# #",
    "# #   #     # #           #",
    "# # #   ####### ###########",
    "# # # #       # #         C",
    "# # ##### ### # # ####### #",
    "# # #     ### # #       # #",
    "#   ##### ### # ######### #",
    "######### ### #           #",
    "# ####### ### #############",
    "A           #   ###   #   #",
    "# ############# ### # # # #",
    "# ###       # # ### # # # #",
    "# ######### # # ### # # # F",
    "#       ### # #     # # # #",
    "# ######### # ##### # # # #",
    "# #######   #       #   # #",
    "# ####### ######### #######",
    "#         #########       #",
    "#######E############D######"
};


void go_maze (int x,int y)
{
    if ( (x < 0) || (x > 24) || (y < 0) || (y > 26) || (maze[x][y] == '#') )
        return;

    if( (maze[x][y] != ' ') && (maze[x][y] != '#') )
    {
        std::cout<<maze[x][y]<<" ";
        number_of_exits++;
    }

    maze[x][y]='#';

    go_maze(x+1, y);
    go_maze(x-1, y);
    go_maze(x, y+1);
    go_maze(x, y-1);

}

int main()
{
    short a,b;
    std::cin>>a>>b;

    if ( (a < 0) || (a > 24) || (b < 0) || (b > 26) || (maze[a][b] == '#') )
        std::cout<<"Incorrect coordinates!\n";
    else
    {
        go_maze(a, b);
        if (number_of_exits == 0)
            std::cout<<"No exit\n";
    }

}
